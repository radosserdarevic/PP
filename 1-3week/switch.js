var day = 3;

switch (day) {
    case 1:
        console.log("Monday!");
        break;
    case 2:
        console.log("Tuesday");
        break;
    case 3:
        console.log("Wendesday!");
        break;
    case 4:
        console.log("Thursday!");
        break;
    case 5:
        console.log("Friday!");
        break;
    case 6:
        console.log("Saturday!");
        break;
    case 7:
        console.log("Sunday!");
        break;
    default:
        console.log("Input must be number between 1 and 7!");
}

